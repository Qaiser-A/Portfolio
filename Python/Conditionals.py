# Conditionals

#Setting the age to 25 and storing it in a variable called "age"
age = 25

#If the age is less than 25
if age < 26:
    #Output the following to the console
    print('You are younger than 26')
#Else if age is less than 24
elif age > 24:
    #Output the following
    print ('You are older than 24')
else:
    print('You clearly have a different age')
